def stringify(map)
  map.map { |row| row.join }.join("\n")
end

def rotate(map)
  map.transpose.each(&:reverse!)
end

def roll(map)
  map.each { |row|
    rolling = 0
    row.each_with_index { |n, i|
      case n
      when 1
        row[i] = 0
        rolling += 1
      when 2
        row[(i-rolling)...i] = Array.new(rolling, 1)
        rolling = 0
      end
    }
    if rolling != 0
      row[(row.length-rolling)..] = Array.new(rolling, 1)
    end
  }
end

def weigh(map)
  map.inject(0) { |weight, row| weight + row.each_with_index.inject(0) { |weight, col| weight + (col[0] == 1 ? col[1] + 1 : 0) } }
end

def cycle(map)
  4.times { map = roll(rotate(map)) }
  map
end

def solve(input)
  map = input.split("\n").map { |line| line.chars.map { |c| c.tr('.O#', '012').to_i } }
  
  i = 0
  h = {}
  rate = 0
  loop do
    map = cycle map
    i += 1

    s = stringify map
    if h.include? s
      rate = i - h[s]
      puts "Repeat detected at cycle #{i}, already seen at #{h[s]}, repeat rate is #{rate}"
      break
    else
      h[s] = i
    end
  end

  ((1000000000 - i) % rate).times { map = cycle map }
  
  p weigh(rotate(map))
end

